void fjeeprom_read(uint8_t *dest, uint32_t adr, uint32_t len) 
{
	uint8_t const cmd = FJEEPROM_READ;
	volatile uint32_t indx, indy;
	volatile uint8_t dataU8 = 0, bit_state;
	
	GPIO_WriteBit(FJEEPROM_CS_PORT, FJEEPROM_CS_PIN, Bit_RESET);	// CS LOW // 
	
	// Send CMD, first MSB // 
	for(indx=7 ;; indx--) {
		GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_RESET);	// SCK LOW // 
		if(cmd & (0x1U<<indx)) 
			GPIO_WriteBit(FJEEPROM_MOSI_PORT, FJEEPROM_MOSI_PIN, Bit_SET);	// MOSI HIGH //
		else
			GPIO_WriteBit(FJEEPROM_MOSI_PORT, FJEEPROM_MOSI_PIN, Bit_RESET);	// MOSI LOW //
		GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_SET);	// SCK HIGH // 
		
		if(0 == indx) 
			break;
	}
	// Send Address, First MSB // 
	for(indx=23 ;; indx--) {
		GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_RESET);	// SCK LOW // 
		if(adr & (0x1U<<indx)) 
			GPIO_WriteBit(FJEEPROM_MOSI_PORT, FJEEPROM_MOSI_PIN, Bit_SET);	// MOSI HIGH //
		else
			GPIO_WriteBit(FJEEPROM_MOSI_PORT, FJEEPROM_MOSI_PIN, Bit_RESET);	// MOSI LOW //
		GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_SET);	// SCK HIGH // 
		
		if(0 == indx) 
			break;
	}

	GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_RESET);	// SCK LOW // 

	// Read data until all datas up to len read // 
	for(indy=0 ; indy<len ; indy++, dest++) {
		dataU8 = 0;
		for(indx=7 ;; indx--) {	// 8 bit data read // 
			GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_SET);	// SCK HIGH // 
			bit_state = GPIO_ReadInputDataBit(FJEEPROM_MISO_PORT, FJEEPROM_MISO_PIN);
			if(bit_state)
				dataU8 |= (0x1U << indx);
			GPIO_WriteBit(FJEEPROM_SCK_PORT, FJEEPROM_SCK_PIN, Bit_SET);	// SCK LOW // 
			
			if(0 == indx) 
				break;
		}
		*dest = dataU8;	// Store read u8 data to destination // 
	}

	GPIO_WriteBit(FJEEPROM_CS_PORT, FJEEPROM_CS_PIN, Bit_SET);	// CS HIGH // 	
}

